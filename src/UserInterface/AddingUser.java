    private void mylistMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mylistMouseClicked
        // TODO add your handling code here:
        
        if(mylist.getSelectedValue()!=null)
        {
            try 
            {
                RemoteDevice device=mapper.get(mylist.getSelectedIndex());
                String Dname=device.getFriendlyName(false);
                String address = Resources.getFormattedAddress(device.getBluetoothAddress());
                isPaired=device.isTrustedDevice();
                Bname.setText(Dname);
                Baddress.setText(address);
                pairstatus.setText(isPaired?"Yes":"No");
                pairedButton.setEnabled(!isPaired);
                addButton.setEnabled(isPaired);
                
            } 
            catch (IOException ex)
            {
            }
        }
    }//GEN-LAST:event_mylistMouseClicked

    private void pairedButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pairedButtonActionPerformed
        // TODO add your handling code here:
        {
            
            RemoteDevice device = mapper.get(mylist.getSelectedIndex());
            isPaired=Bluetooth.pairing(device);
            if(isPaired) 
            {
                JOptionPane.showMessageDialog(this,"Device successfully paired");
                pairedButton.setEnabled(false);
                addButton.setEnabled(true);
            }
        }
    }//GEN-LAST:event_pairedButtonActionPerformed

