    private void search()
    {  
        
        this.setCursor(new java.awt.Cursor(java.awt.Cursor.WAIT_CURSOR));
        try 
        {
            bluetooth = new Bluetooth();
            client=bluetooth.searchDevices();
        } 
        catch (BluetoothStateException ex) 
        {
            JOptionPane.showMessageDialog(this,"Please turn on the bluetooth first !!!");
            dispose();
        } 
        catch (InterruptedException ex) 
        {
            
        }
        int i=0;
        mylist.setModel(model);
        for(RemoteDevice x: client)
        {
            
            try
            {
                model.addElement(x.getFriendlyName(false));
            }
            catch(IOException e)
            {
                model.addElement(x.getBluetoothAddress());
            }
            finally
            {
                mapper.put(i, x);
                i++;
            }
        }
        searching.setText("Search Completed! Choose device");
        this.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
//        refresh.setVisible(true);
    }
