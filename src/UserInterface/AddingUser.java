    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        User user = new User(Bname.getText(),Baddress.getText(),admin.isSelected());
        try
        {
            Users.addUser(user);
            if(adminPanel==null)
            {
                Log log = new Log("root","Admin added : "+user.getName());
                Logs.addLog(log);
                JOptionPane.showMessageDialog(this,"Admin Successfully added ! Restart the app");
                System.exit(0);   // suggest : if any problem arises then use dispose instead of exit
            }      
            else
            {
                String type=user.isType()?"Admin":"Naive user";
                Log log = new Log(adminPanel.getUser().getName(),type+" added : "+user.getName());
                Logs.addLog(log);
                JOptionPane.showMessageDialog(this,"New "+type+" Successfully added !");
                this.adminPanel.setVisible(true);
                dispose();
            }
        } 
        catch (IOException ex) 
        {
            JOptionPane.showMessageDialog(this,"Something bad happened !!! ");  
            if(adminPanel!=null)
            {
                dispose();
                this.adminPanel.setVisible(true);
            }      
        }
        
    }//GEN-LAST:event_addButtonActionPerformed

